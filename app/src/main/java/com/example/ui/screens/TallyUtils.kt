@OptIn(ExperimentalAnimationApi::class)
// Global safe monetary formatter
fun formatCurrency(amount: Double): String {
    return try {
        val format = NumberFormat.getNumberInstance(Locale.US)
        format.format(amount)
    } catch (_: Exception) {
        amount.toString()
    }
}
